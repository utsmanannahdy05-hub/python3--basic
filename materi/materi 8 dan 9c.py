# FUNGSI LUAS PERSEGI PANJANG 
def luas_persegi_panjang(panjang, lebar):
  print("P = ", panjang)
  print("L = ",lebar)
  rumus = panjang * lebar
  print("hasil luas persegi panjang =", rumus)
# Memanggil fungsi dengan contoh nilai
luas_persegi_panjang(5, 3)

